a = 10
b = 3

# Addition
add = a + b  # 13

# Subtraction
sub = a - b  # 7

# Multiplication
mul = a * b  # 30

# Division
div = a / b  # 3.333...

# Floor Division
floor_div = a // b  # 3

# Modulus
mod = a % b  # 1

# Exponentiation
exp = a ** b  # 1000

print("Add:",add,"Sub:",sub,"Mul:",mul,"Div:",div,"Floor",floor_div,"Modulas:",mod,"Exponentiation:",exp)